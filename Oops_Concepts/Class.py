'''
Class is a template
It's a blue print of an object

Syntax:

class class_name():

Example:

class chaitanya():

'''
class chaitanya():
    a = 2
    def func1(self):
        print(self.a)

obj1 = chaitanya()

obj1.func1() # // calling by using function name
print(obj1.a) # calling by using variable name